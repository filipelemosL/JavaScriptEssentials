let hora = 15;

// Condicional
if (hora < 12) {
  console.log("Bom dia!");
} else if (hora >= 12 && hora < 18) {
  console.log("Boa tarde!");
} else {
  console.log("Boa noite!");
}

// Loops
for (let i = 10; i < 20; i++) {
  console.log(i);
}

let j = 0;
while (j < 5) {
  console.log(j);
  j++;
}

